const catalogs = { en, pt_BR };

function t(locale, key, vars = {}) {
    const resolved = normalizeLocale(locale) || "en";
    const template = (catalogs[resolved] && catalogs[resolved][key]) || en[key] || key;
    return String(template).replace(/\{(\w+)\}/g, (match, name) => vars[name] === undefined ? match : vars[name]);
}

function htmlLang(locale) {
    return normalizeLocale(locale) === "pt_BR" ? "pt-BR" : "en";
}

module.exports = { catalogs, htmlLang, t };
